export type IconName = keyof typeof ICONS;

export interface IconOptions {
  size?: number;
  className?: string;
}

/**
 * Get icon HTML with optional size override
 */
export function getIcon(name: IconName, options: IconOptions = {}): string {
  const icon = ICONS[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }

  let result: string = icon;

  if (options.size) {
    result = result
      .replace(/width="\d+"/, `width="${options.size}"`)
      .replace(/height="\d+"/, `height="${options.size}"`);
  }

  if (options.className) {
    result = result.replace('<svg', `<svg class="${options.className}"`);
  }

  return result;
}
